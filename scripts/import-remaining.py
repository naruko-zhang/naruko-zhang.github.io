def create_post(post_data):
    doc_id = uuid.uuid4().hex[:20]
    payload = {
        "documentId": doc_id,
        "data": post_data,
        "permissions": ['read("any")', 'update("users")', 'delete("users")']
    }
    body = json.dumps(payload).encode('utf-8')
    req = urllib.request.Request(BASE, data=body, headers=HEADERS, method='POST')
    try:
        with urllib.request.urlopen(req) as resp:
            result = json.loads(resp.read())
            return result.get('$id') is not None
    except urllib.error.HTTPError as e:
        err = json.loads(e.read())
        msg = err.get('message', '')
        if 'already exists' in msg or 'unique' in msg.lower():
            return 'exists'
        print(f"  HTTP error: {msg[:80]}")
        return False

