// 从 appwrite-cf 缓存中读取 session token
function getSessionToken() {
  const possiblePaths = [
    process.env.HOME + '/.appwrite-cf/session',
    '/root/.appwrite-cf/session',
    process.env.HOME + '/.config/appwrite-cf/session',
  ]

  for (const p of possiblePaths) {
    if (fs.existsSync(p)) {
      return fs.readFileSync(p, 'utf-8').trim()
    }
  }
  return null
}

// 读取登录信息（appwrite-cf 存放在 ~/.appwrite-cf 目录下）
function getCredentials() {
  const configDir = process.env.HOME + '/.appwrite-cf'
  const globalFile = configDir + '/global.json'

  if (fs.existsSync(globalFile)) {
    try {
      const data = JSON.parse(fs.readFileSync(globalFile, 'utf-8'))
      return data
    } catch (_) {}
  }
  return null
}

